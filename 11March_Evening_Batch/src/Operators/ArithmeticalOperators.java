package Operators;

public class ArithmeticalOperators {

	public static void main(String[] args) {
		
		//Operators: Characters that is used to perform specific operation is called as operators.
		//Arithmetic Operators: +,-,*,/,% (Modulus: denotes remainder of the given division as o/p.
		
		int a = 50;
		int b = 10;
		
		System.out.println(a+b);//60
		System.out.println(a-b);//40
		System.out.println(a*b);//500
		System.out.println(a/b);//5
		System.out.println(a%b);//50%10 is 0 as remainder is 0
		
	}
}
