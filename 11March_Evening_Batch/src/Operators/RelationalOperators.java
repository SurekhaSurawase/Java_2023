package Operators;

public class RelationalOperators {

	public static void main(String[] args) {
		//Relational Operators: <,>,<=,>=,==,!=;
		
		int A = 40;
		int B = 20;
		
		System.out.println(A<B);
		System.out.println(A>B);
		System.out.println(A<=B);
		System.out.println(A>=B);
		System.out.println(A==B);
		System.out.println(A!=B);
		
		
	}
}
