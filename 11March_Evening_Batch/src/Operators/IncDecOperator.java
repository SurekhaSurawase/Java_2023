package Operators;

public class IncDecOperator {

	public static void main(String[] args) {
		//Increment and Decrement Operator
		//Value increases or decreases by 1
		
		int i = 10;
		i++;           //Increment by 1; 11
		System.out.println(i);
		i =i+2; //13
		System.out.println(i);
		i--; //12
		System.out.println(i);
	}
}
