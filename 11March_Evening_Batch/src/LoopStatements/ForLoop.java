package LoopStatements;

public class ForLoop {

	public static void main(String[] args) {
	//Loops are used to use same set of data repeatedly which evaluates when a condition is true.
		//4 types: for loop, while loop, do while loop, Advance for loop/ For each loop
		
		//for loop: Syntax: 
		/*for(initialization, condition, inc/dec){
			//code to be executed.
		}*/
		
		//Write a program to print 1 to 10 on o/p screen.
		
		for(int i=1; i<=10; i++) {
			System.out.println(i);
		}
		System.out.println("Loop completed");
	}
}
