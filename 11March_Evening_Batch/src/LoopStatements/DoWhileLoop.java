package LoopStatements;

public class DoWhileLoop {

	public static void main(String[] args) {
		//do-while Loop
//
//		Syntax:
//			intialization;
//		do {
//			statements;
//			inc/dec;
//		}
//		while(condition);
		
		
		//WAP to print 1 to 10 using do-while loop
		int i=1;
		do {
			System.out.println(i);
			i++;
		}
		while(i<=10);
	}
}
