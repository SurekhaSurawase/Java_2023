package LoopStatements;

public class InifiniteLoopForLoop {

	public static void main(String[] args) {
		//Infinite loop conditions for loop
		
		for(int i=1;i<=10; ) {//here inc/dec operator is absent, value of i will be 1 always hence loop will execute infinite
			System.out.println("HI");
		}
		System.out.println("Loop completed");
	}
}
