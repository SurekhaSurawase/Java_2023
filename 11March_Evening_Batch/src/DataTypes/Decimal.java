package DataTypes;

public class Decimal {

	public static void main(String[] args) {
		//5. Float: Used to store decimal value. Size is 4 byte i.e 32 bit
		//Float can store integer value but int can not store float value.
		
		float f = 458;
		float g = 4578.25f;
		System.out.println("Float Data type: "+f);
		System.out.println("Float Data type: "+g);
		System.out.println();
		
		//6. Double Data type: size is 8 byte i.e 64 bit
		//Syntax: double d = 654;
		
		double d = 1446237987;
		double e = 874456;
		System.out.println("Double Data type: "+d);
		System.out.println("Double Data Type: "+e);
		

	}
}
