package Casting;

public class PrimitiveCasting {

	public static void main(String[] args) {
		//Casting: is converting one type of info into another type
		
		//1.primitive type casting i.e implicit casting i.e casting of lower sized datatype into higher one.
		
		int number = 100;
		
		System.out.println(number);
		
		double D1 = number;
		
		System.out.println(D1);
	}
}
